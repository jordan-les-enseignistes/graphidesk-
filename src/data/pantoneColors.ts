// Fonctions utilitaires
export function findPantoneByCode(searchCode: string): PantoneColor | null {
  const normalized = searchCode.trim().toUpperCase();
  return pantoneColors.find(
    (c) => c.code.toUpperCase() === normalized ||
           c.code.toUpperCase().replace(/\s+/g, '') === normalized.replace(/\s+/g, '')
  ) || null;
}

export function searchPantoneByCode(query: string): PantoneColor[] {
  const normalized = query.trim().toLowerCase();
  if (!normalized) return [];

  return pantoneColors.filter((c) =>
    c.code.toLowerCase().includes(normalized)
  ).slice(0, 50);
}
